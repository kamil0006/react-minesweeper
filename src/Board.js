import React, { useState, useEffect } from 'react';
import OpenAI from 'openai';
import './Board.css';

const openai = new OpenAI({
	apiKey: process.env.REACT_APP_OPENAI_API_KEY,
	dangerouslyAllowBrowser: true,
});

const BOARD_SIZE = 10;
const MINES_COUNT = 13;

const generateBoard = () => {
	let board = Array(BOARD_SIZE)
		.fill()
		.map(() => Array(BOARD_SIZE).fill({ isMine: false, revealed: false, flagged: false, count: 0 }));

	// Losowanie min
	let minesPlaced = 0;
	while (minesPlaced < MINES_COUNT) {
		let x = Math.floor(Math.random() * BOARD_SIZE);
		let y = Math.floor(Math.random() * BOARD_SIZE);
		if (!board[x][y].isMine) {
			board[x][y] = { ...board[x][y], isMine: true };
			minesPlaced++;
		}
	}

	// Obliczanie liczb wok√≥≈Ç min
	for (let x = 0; x < BOARD_SIZE; x++) {
		for (let y = 0; y < BOARD_SIZE; y++) {
			if (!board[x][y].isMine) {
				let count = 0;
				[-1, 0, 1].forEach(dx => {
					[-1, 0, 1].forEach(dy => {
						let nx = x + dx,
							ny = y + dy;
						if (
							!(dx === 0 && dy === 0) &&
							nx >= 0 &&
							ny >= 0 &&
							nx < BOARD_SIZE &&
							ny < BOARD_SIZE &&
							board[nx][ny].isMine
						) {
							count++;
						}
					});
				});
				board[x][y] = { ...board[x][y], count };
			}
		}
	}

	return board;
};

// Poprawione ods≈Çanianie pustych p√≥l (`count === 0`)
const revealAdjacent = (board, x, y) => {
	let newBoard = board.map(row => row.map(cell => ({ ...cell })));
	const queue = [[x, y]];
	const visited = new Set();

	while (queue.length > 0) {
		const [cx, cy] = queue.shift();
		const key = `${cx},${cy}`;
		if (visited.has(key) || newBoard[cx][cy].revealed) continue;

		newBoard[cx][cy].revealed = true;
		visited.add(key);

		if (newBoard[cx][cy].count === 0) {
			[-1, 0, 1].forEach(dx => {
				[-1, 0, 1].forEach(dy => {
					let nx = cx + dx,
						ny = cy + dy;
					if (nx >= 0 && ny >= 0 && nx < BOARD_SIZE && ny < BOARD_SIZE && !visited.has(`${nx},${ny}`)) {
						queue.push([nx, ny]);
					}
				});
			});
		}
	}
	return newBoard;
};

const Board = () => {
	const [board, setBoard] = useState(generateBoard());
	const [gameOver, setGameOver] = useState(false);
	const [gameMessage, setGameMessage] = useState('');
	const [time, setTime] = useState(0);
	const [isRunning, setIsRunning] = useState(false);
	const [aiRunning, setAiRunning] = useState(false);

	useEffect(() => {
		let timer;
		if (isRunning && !gameOver) {
			timer = setInterval(() => {
				setTime(prevTime => prevTime + 1);
			}, 1000);
		} else {
			clearInterval(timer);
		}

		return () => clearInterval(timer);
	}, [isRunning, gameOver]);

	const revealCell = (x, y) => {
		console.log(`üü¢ AI pr√≥buje kliknƒÖƒá (${x}, ${y})`);

		if (!isRunning) {
			setIsRunning(true);
		}

		if (gameOver || board[x][y].revealed || board[x][y].flagged) return;

		let newBoard = board.map(row => row.map(cell => ({ ...cell })));

		if (newBoard[x][y].isMine) {
			setGameOver(true);
			setIsRunning(false);
			setGameMessage('üí• Game Over! üí•');
			newBoard = newBoard.map(row => row.map(cell => (cell.isMine ? { ...cell, revealed: true } : cell)));
			setBoard(newBoard);
			return;
		} else {
			newBoard = revealAdjacent(newBoard, x, y);
		}

		// üîπ Sprawdzamy wygranƒÖ na nowej planszy przed jej zapisaniem do stanu
		if (checkWin(newBoard)) {
			console.log('üèÜ Gra wygrana! Wszystkie pola bez min sƒÖ odkryte.');
			setGameMessage('üéâ Gratulacje! Wygra≈Çe≈õ! üéâ');
			setIsRunning(false);
			setGameOver(true);
		}

		setBoard(newBoard); // Aktualizujemy stan planszy
	};

	// Sprawdzanie wygranej
	const checkWin = (currentBoard = board) => {
		if (!Array.isArray(currentBoard)) {
			console.error('‚ùå B≈ÇƒÖd: przekazano niepoprawnƒÖ planszƒô do checkWin()', currentBoard);
			return false;
		}

		let allNonMinesRevealed = true;
		let allMinesFlagged = true;

		for (let row of currentBoard) {
			for (let cell of row) {
				if (!cell.isMine && !cell.revealed) {
					allNonMinesRevealed = false; // Je≈õli istnieje zakryte pole bez miny, gra jeszcze trwa
				}
				if (cell.isMine && !cell.flagged) {
					allMinesFlagged = false; // Je≈õli jaka≈õ mina nie jest oznaczona, gra jeszcze trwa
				}
			}
		}

		if (allNonMinesRevealed || allMinesFlagged) {
			console.log('üèÜ Gra wygrana! Wszystkie pola bez min sƒÖ odkryte lub miny sƒÖ poprawnie oznaczone.');
			setGameMessage('üéâ Wygra≈Çe≈õ! üéâ');
			setAiRunning(false);
			setIsRunning(false); // ‚è≥ Zatrzymanie czasu po wygranej
			setGameOver(true);
			return true;
		}

		return false;
	};

	const toggleFlag = (e, x, y) => {
		e.preventDefault(); // Blokujemy domy≈õlne menu kontekstowe

		if (gameOver || board[x][y].revealed) return;

		let newBoard = board.map(row => row.map(cell => ({ ...cell })));
		newBoard[x][y].flagged = !newBoard[x][y].flagged; // Prze≈ÇƒÖczamy flagƒô

		setBoard(newBoard); // Aktualizujemy stan planszy

		// üîπ Sprawdzamy wygranƒÖ po KA≈ªDEJ zmianie flagi
		setTimeout(() => {
			if (checkWin(newBoard)) {
				setAiRunning(false);
			}
		}, 100);
	};

	const getRandomMove = () => {
		let availableMoves = [];

		for (let x = 0; x < BOARD_SIZE; x++) {
			for (let y = 0; y < BOARD_SIZE; y++) {
				if (!board[x][y].revealed && !board[x][y].flagged) {
					availableMoves.push([x, y]);
				}
			}
		}

		if (availableMoves.length === 0) {
			console.error('‚ö†Ô∏è Brak dostƒôpnych ruch√≥w! AI ko≈Ñczy grƒô.');
			setAiRunning(false);
			return '0,0';
		}

		let [rx, ry] = availableMoves[Math.floor(Math.random() * availableMoves.length)];
		console.log(`üé≤ AI wybiera losowe pole: (${rx}, ${ry})`);
		return `${rx},${ry}`;
	};

	const getSafestMove = () => {
		let bestMove = null;
		let minRisk = Infinity;

		for (let x = 0; x < BOARD_SIZE; x++) {
			for (let y = 0; y < BOARD_SIZE; y++) {
				if (!board[x][y].revealed && !board[x][y].flagged) {
					let surroundingMines = 0;
					let surroundingCovered = 0;

					[-1, 0, 1].forEach(dx => {
						[-1, 0, 1].forEach(dy => {
							let nx = x + dx,
								ny = y + dy;
							if (nx >= 0 && ny >= 0 && nx < BOARD_SIZE && ny < BOARD_SIZE) {
								if (board[nx][ny].isMine) surroundingMines++;
								if (!board[nx][ny].revealed) surroundingCovered++;
							}
						});
					});

					let riskFactor = surroundingMines + surroundingCovered;

					if (riskFactor < minRisk) {
						minRisk = riskFactor;
						bestMove = [x, y];
					}
				}
			}
		}

		if (!bestMove) {
			console.warn('‚ö†Ô∏è AI nie znalaz≈Ço bezpiecznego ruchu ‚Äì wybiera losowe pole.');
			return getRandomMove();
		}

		console.log(`üõ°Ô∏è AI wybiera najbezpieczniejsze pole: (${bestMove[0]}, ${bestMove[1]})`);
		return `${bestMove[0]},${bestMove[1]}`;
	};

	const handleAIFailure = () => {
		console.warn("‚ö†Ô∏è OpenAI nie odpowiedzia≈Ço ‚Äì AI wybiera bezpieczny lub losowy ruch.");
	
		if (gameOver || checkWin()) { 
			console.log("üèÅ AI zatrzymane po zako≈Ñczeniu gry.");
			setAiRunning(false);
			return;
		}
	
		let move = getSafestMove(); 
	
		if (!move) {
			console.warn("‚ö†Ô∏è AI nie znalaz≈Ço ≈ºadnego bezpiecznego pola ‚Äì wybiera losowy ruch.");
			move = getRandomMove();
		}
	
		let [x, y] = move.split(',').map(Number);
	
		console.log("üéØ AI klika:", x, y);
		revealCell(x, y);
	
		setTimeout(() => {
			if (!gameOver && !checkWin()) {
				console.log("üîÑ AI wykonuje kolejny ruch...");
				askOpenAI();
			} else {
				console.log("üèÅ AI zako≈Ñczy≈Ço dzia≈Çanie.");
				setAiRunning(false);
			}
		}, 500);
	};
	

	const generatePrompt = () => {
		return `
			Jeste≈õ **ekspertem w grze Minesweeper**. Twoim zadaniem jest graƒá **jak do≈õwiadczony gracz**, u≈ºywajƒÖc **logiki i analizy prawdopodobie≈Ñstwa**.
	
			**üîπ Oto zasady twojej strategii:**
			1Ô∏è‚É£ **Flaga pewnych min:**  
				- Je≈õli liczba zakrytych sƒÖsiad√≥w = liczbie na polu, oznacz je jako miny üö©.  
			2Ô∏è‚É£ **Ods≈Çanianie pewnych p√≥l:**  
				- Je≈õli liczba oznaczonych min wok√≥≈Ç pola = liczbie na polu, odkryj pozosta≈Çe pola.  
			3Ô∏è‚É£ **Analiza ryzyka:**  
				- Je≈õli nie masz pewnego ruchu, **znajd≈∫ pole z najmniejszym prawdopodobie≈Ñstwem miny** i kliknij je.  
			4Ô∏è‚É£ **Zgadywanie (w ostateczno≈õci):**  
				- Je≈õli nie mo≈ºesz obliczyƒá ruchu, wybierz **najbezpieczniejsze pole w otwartym obszarze**.  
			5Ô∏è‚É£ **Nigdy nie klikaj pola, kt√≥re mo≈ºe byƒá minƒÖ, je≈õli masz innƒÖ opcjƒô!**  
	
			**üîπ Aktualna plansza:**
			- **?** = Zakryte pole  
			- **üö©** = Flaga (oznaczona mina)  
			- **üí£** = Mina  
			- **Cyfra** = Liczba min w sƒÖsiedztwie  
	
			**üîπ Twoje zadanie:**  
			- Zwr√≥ƒá najlepszy mo≈ºliwy ruch w formacie X,Y (np. 3,4).  
			- Je≈õli musisz oznaczyƒá minƒô, zwr√≥ƒá FLAG X,Y.  
			- Nie podawaj dodatkowych informacji  tylko ruch.
	
			**üü¢ Aktualna sytuacja na planszy:**  
			${board
				.map(row =>
					row.map(cell => (cell.revealed ? (cell.isMine ? 'üí£' : cell.flagged ? 'üö©' : cell.count) : '?')).join(' ')
				)
				.join('\n')}
		`;
	};

	let moveCounter = 0; // Licznik ruch√≥w AI, aby unikaƒá zapƒôtlenia

	const askOpenAI = async () => {
		console.log("‚úÖ AI startuje i analizuje planszƒô...");
	
		if (gameOver || checkWin()) { 
			console.log("üèÅ AI zako≈Ñczy≈Ço dzia≈Çanie. Gra siƒô sko≈Ñczy≈Ça.");
			setAiRunning(false);
			return;
		}
	
		moveCounter++;
		if (moveCounter > 50) {
			console.error("‚ö†Ô∏è AI wykona≈Ço za du≈ºo ruch√≥w ‚Äì zatrzymuje siƒô.");
			setAiRunning(false);
			return;
		}
	
		console.log("üì° Wysy≈Çanie zapytania do OpenAI...");
	
		let aiTimeout = setTimeout(() => {
			console.warn("‚è≥ AI za d≈Çugo my≈õli ‚Äì wybiera bezpieczny ruch.");
			handleAIFailure();
		}, 2500); // AI ma maksymalnie 2.5 sekundy na decyzjƒô
	
		try {
			const response = await openai.chat.completions.create({
				model: 'gpt-4o-mini',
				messages: [{ role: 'system', content: generatePrompt() }],
				max_tokens: 10,
			});
	
			clearTimeout(aiTimeout); 
	
			console.log("üîÑ OpenAI odpowiedzia≈Ço:", response);
	
			let move = response.choices?.[0]?.message?.content?.trim();
			console.log("üß† AI wybra≈Ço ruch:", move);
	
			if (!move || (!move.includes(",") && !move.includes("FLAG"))) {
				console.warn("‚ö†Ô∏è OpenAI zwr√≥ci≈Ço b≈Çƒôdne dane lub brak ruchu. AI wybiera losowy ruch.");
				handleAIFailure();
				return;
			}
	
			if (gameOver || checkWin()) { 
				console.log("üèÅ AI zatrzymane po zako≈Ñczeniu gry.");
				setAiRunning(false);
				return;
			}
	
			if (move.includes("FLAG")) {
				let parts = move.split(' ');
				let x = parseInt(parts[1]);
				let y = parseInt(parts[2]);
	
				console.log(`üö© AI oznacza minƒô: (${x}, ${y})`);
				toggleFlag(null, x, y);
			} else {
				let [x, y] = move.split(',').map(Number);
				if (isNaN(x) || isNaN(y) || board[x][y].revealed || board[x][y].flagged) {
					console.error("‚ùå OpenAI zwr√≥ci≈Ço niepoprawne lub ju≈º odkryte pole. AI wybiera bezpieczny ruch.");
					handleAIFailure();
					return;
				}
				console.log("üéØ AI klika:", x, y);
				revealCell(x, y);
			}
	
			setTimeout(() => {
				if (!gameOver && !checkWin()) {
					console.log("üîÑ AI wykonuje kolejny ruch...");
					askOpenAI();
				} else {
					console.log("üèÅ AI zako≈Ñczy≈Ço dzia≈Çanie.");
					setAiRunning(false);
				}
			}, 500);
	
		} catch (error) {
			console.error("‚ùå B≈ÇƒÖd komunikacji z OpenAI:", error);
			handleAIFailure();
		}
	};
	

	const startAI = () => {
		if (gameOver || aiRunning) {
			console.log('‚ö†Ô∏è AI nie mo≈ºe siƒô uruchomiƒá: gameOver =', gameOver, ', aiRunning =', aiRunning);
			return;
		}
		console.log('ü§ñ AI uruchomione!');
		setAiRunning(true);
		askOpenAI();
	};

	const resetGame = () => {
		console.log('üîÑ Resetowanie gry...');
		setBoard(generateBoard()); // Tworzymy nowƒÖ planszƒô
		setGameOver(false);
		setGameMessage('');
		setTime(0);
		setIsRunning(false);
		setAiRunning(false);
		moveCounter = 0; // Resetujemy licznik ruch√≥w AI
	};

	return (
		<div className='board-container'>
			<h1>Minesweeper</h1>
			<button onClick={resetGame} className='reset-button'>
				üîÑ Restart
			</button>
			<button onClick={startAI} className='ai-button'>
				ü§ñ Start AI
			</button>

			{gameMessage && <h2 className='game-message'>{gameMessage}</h2>}
			<p className='timer'>‚è≥ Czas: {time}s</p>
			<div className='board'>
				{board.map((row, x) => (
					<div key={x} className='row'>
						{row.map((cell, y) => (
							<div
								key={y}
								className={`cell ${cell.revealed ? 'revealed' : ''} ${cell.isMine && gameOver ? 'mine' : ''}`}
								onClick={() => revealCell(x, y)}
								onContextMenu={e => toggleFlag(e, x, y)} // Dodajemy obs≈Çugƒô prawego klikniƒôcia
								data-count={cell.count}>
								{cell.flagged ? 'üö©' : cell.revealed ? (cell.isMine ? 'üí£' : cell.count || '') : ''}
							</div>
						))}
					</div>
				))}
			</div>
		</div>
	);
};

export default Board;
